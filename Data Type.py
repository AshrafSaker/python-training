# Single Data Type
strings = "Text" #str
intgers = 10   #int
flot = 10.51 #Float
boolen = 5 > 6   #True or False
print(type(strings))
print(type(intgers))
print(type(flot))
print(type(boolen))
print(boolen)
print(strings , boolen)
print(type(boolen))
st = str(boolen)
print(type(st))
print(strings , st)
print(strings , intgers)
print(intgers + flot)
print(intgers , flot)
print("10"+"10.51")
intgers = 30.5
print(intgers)
